from ast import literal_eval

def translate_rna(filepath, rna_string):
	f = open(filepath, "r")
	condontable = f.read()
	RNA_dict = literal_eval(condontable)
	f.close()
	modified = ' '.join([rna_string[i:i+3] for i in range(0, len(rna_string), 3)])
	
	for key in RNA_dict.keys():
		mod_rna_dict = modified.replace(key, RNA_dict[key])
	final_protein = mod_rna_dict.replace(" ", "")
	
